# 시간초과

n,k = map(int, input().split())

arr = [[0] * (n+1) for _ in range(k+1)]

for i in range(1, n+1):
    arr[1][i] = 1

for i in range(1, k+1):
    arr[i][1] = i
    for j in range(2, n+1):
        arr[i][j] = (arr[i][j-1] + arr[i-1][j]) % 1000000000
# print(arr)

print(arr[k][n])